<template>
    <bk-exception class="exception-wrap-item" type="404">
        <div>
            <span v-if="pageRoute.pageName">{{ $t('未找到页面，请检查页面“{0}”的路由配置', [pageRoute.pageName]) }}</span>
            <span v-else-if="pageRoute.pageCode === ''">{{ $t('未找到应用首页，建议将地址“/”配置为应用默认首页') }}</span>
            <span v-else-if="!pageRoute.pageCode">{{ $t('未找到页面') }}</span>
        </div>
    </bk-exception>
</template>

<script>
    import { mapState } from 'vuex'
    export default {
        data () {
            return {}
        },
        computed: {
            ...mapState(['projectPageRouteList']),
            pageRoute () {
                const pageCode = this.$route.query.pageCode // '' | undefined | code
                const pageRoute = this.projectPageRouteList.find(item => item.pageCode === pageCode)
                return pageRoute || { pageCode }
            }
        }
    }
</script>

<style scoped>
    .exception-wrap-item {
        margin: 150px auto 0 !important;
        height: 420px;
        padding-top: 22px;
    }
</style>
